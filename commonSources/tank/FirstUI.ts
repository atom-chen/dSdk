const {ccclass, property} = cc._decorator;

@ccclass
export default class FirstUI extends cc.Component {

    @property(cc.Node)
    quan:cc.Node = null;
    
    start() {
        cc.director.loadScene("mainScene");
    }
    update(n) {
        this.quan && (this.quan.rotation += 90 * n);
    }


}
